#include "includes/libft.h"

int		ft_height(char *s, char c)
{
	int i;
	int y;

	i = 0;
	y = 0;
	while (s[i] == c)
		i++;
	while (s[i++] != '\0')
	{
		if (s[i] == c)
		{
			y++;
			while (s[i] == c)
				i++;
		}
	}
	i--;
	if (s[i - 1] != c)
		y++;
	return (y);
}
